class ReportGenerator:
    """
    报告生成器类.

    负责生成 POC 验证报告,包括:
    - HTML 格式报告(带图表)
    - JSON 格式报告(结构化数据)
    - PDF 格式报告(可打印)
    - 执行摘要生成
    - 漏洞详情列表
    - 统计信息
    - 自定义模板支持
    - 多语言支持

    Attributes:
        report_templates: 报告模板字典.
        custom_templates: 自定义模板字典.
        enabled_sections: 启用的报告章节集合.
        i18n: 多语言支持实例.
    """

    def __init__(self):
        self.report_templates: Dict[str, Callable] = {
            "html": self._generate_html_report,
            "json": self._generate_json_report,
            "pdf": self._generate_pdf_report
        }
        self.custom_templates: Dict[str, CustomTemplate] = {}
        self.enabled_sections: Set[str] = {
            "executive_summary",
            "severity_distribution_chart",
            "vulnerability_timeline_chart",
            "target_risk_heatmap",
            "verification_details",
            "statistics",
            "recommendations"
        }
        self.i18n = I18N()
        
        logger.info("✅ 报告生成器初始化完成")
    
    def register_custom_template(self, template: CustomTemplate) -> None:
        """
        注册自定义模板
        
        Args:
            template: 自定义模板对象
        """
        if template.base_template and template.base_template in self.custom_templates:
            base = self.custom_templates[template.base_template]
            merged_vars = deepcopy(base.variables)
            merged_vars.update(template.variables)
            template.variables = merged_vars
            
            merged_sections = deepcopy(base.sections)
            merged_sections.update(template.sections)
            template.sections = merged_sections
            
            if not template.html_template:
                template.html_template = base.html_template
        
        self.custom_templates[template.name] = template
        logger.info(f"✅ 已注册自定义模板: {template.name}")
    
    def unregister_custom_template(self, template_name: str) -> bool:
        """
        注销自定义模板
        
        Args:
            template_name: 模板名称
            
        Returns:
            bool: 是否成功注销
        """
        if template_name in self.custom_templates:
            del self.custom_templates[template_name]
            logger.info(f"✅ 已注销自定义模板: {template_name}")
            return True
        return False
    
    def get_template_variables(self, template_name: str) -> Dict[str, Any]:
        """
        获取模板变量
        
        Args:
            template_name: 模板名称
            
        Returns:
            Dict: 模板变量字典
        """
        if template_name in self.custom_templates:
            return {k: v.value for k, v in self.custom_templates[template_name].variables.items()}
        return {}
    
    def set_template_variable(self, template_name: str, var_name: str, value: Any) -> bool:
        """
        设置模板变量
        
        Args:
            template_name: 模板名称
            var_name: 变量名
            value: 变量值
            
        Returns:
            bool: 是否成功设置
        """
        if template_name in self.custom_templates:
            if var_name in self.custom_templates[template_name].variables:
                self.custom_templates[template_name].variables[var_name].value = value
                return True
            else:
                self.custom_templates[template_name].variables[var_name] = TemplateVariable(
                    name=var_name,
                    value=value
                )
                return True
        return False
    
    def enable_section(self, section_id: str) -> None:
        """启用报告章节"""
        self.enabled_sections.add(section_id)
    
    def disable_section(self, section_id: str) -> None:
        """禁用报告章节"""
        self.enabled_sections.discard(section_id)
    
    def is_section_enabled(self, section_id: str) -> bool:
        """检查章节是否启用"""
        return section_id in self.enabled_sections
    
    def set_language(self, language: Language) -> None:
        """设置报告语言"""
        self.i18n.set_language(language)
    
    async def generate_report(
        self,
        verification_task: POCVerificationTask,
        format: str = "html",
        output_path: Optional[str] = None,
        template_name: Optional[str] = None
    ) -> str:
        """
        生成 POC 验证报告
        
        Args:
            verification_task: POC 验证任务对象
            format: 报告格式(html, json, pdf)
            output_path: 输出文件路径,None 则返回内容
            template_name: 自定义模板名称
            
        Returns:
            str: 报告内容或文件路径
        """
        logger.info(f"📄 开始生成报告,格式: {format}, 任务: {verification_task.poc_name}")
        
        results = await POCVerificationResult.filter(
            verification_task=verification_task.id
        ).order_by("-created_at")
        
        if format not in self.report_templates:
            raise ValueError(f"不支持的报告格式: {format}")
        
        custom_template = None
        if template_name and template_name in self.custom_templates:
            custom_template = self.custom_templates[template_name]
        
        report_content = await self.report_templates[format](
            verification_task,
            results,
            custom_template=custom_template
        )
        
        if output_path:
            Path(output_path).parent.mkdir(parents=True, exist_ok=True)
            mode = 'wb' if format == 'pdf' and isinstance(report_content, bytes) else 'w'
            encoding = None if mode == 'wb' else 'utf-8'
            with open(output_path, mode, encoding=encoding) as f:
                f.write(report_content)
            logger.info(f"✅ 报告已保存: {output_path}")
            return output_path
        else:
            return report_content
    
    async def generate_batch_report(
        self,
        verification_tasks: List[POCVerificationTask],
        format: str = "html",
        output_path: Optional[str] = None,
        template_name: Optional[str] = None
    ) -> str:
        """
        生成批量 POC 验证报告
        
        Args:
            verification_tasks: POC 验证任务列表
            format: 报告格式(html, json, pdf)
            output_path: 输出文件路径,None 则返回内容
            template_name: 自定义模板名称
            
        Returns:
            str: 报告内容或文件路径
        """
        logger.info(f"📄 开始生成批量报告,格式: {format}, 任务数: {len(verification_tasks)}")
        
        all_results = []
        for task in verification_tasks:
            results = await POCVerificationResult.filter(
                verification_task=task.id
            ).order_by("-created_at")
            all_results.extend(results)
        
        if format not in self.report_templates:
            raise ValueError(f"不支持的报告格式: {format}")
        
        custom_template = None
        if template_name and template_name in self.custom_templates:
            custom_template = self.custom_templates[template_name]
        
        report_content = await self.report_templates[format](
            verification_tasks,
            all_results,
            is_batch=True,
            custom_template=custom_template
        )
        
        if output_path:
            Path(output_path).parent.mkdir(parents=True, exist_ok=True)
            mode = 'wb' if format == 'pdf' and isinstance(report_content, bytes) else 'w'
            encoding = None if mode == 'wb' else 'utf-8'
            with open(output_path, mode, encoding=encoding) as f:
                f.write(report_content)
            logger.info(f"✅ 批量报告已保存: {output_path}")
            return output_path
        else:
            return report_content
    
    async def merge_reports(
        self,
        reports: List[Dict[str, Any]],
        format: str = "html",
        output_path: Optional[str] = None
    ) -> str:
        """
        合并多个报告
        
        Args:
            reports: 报告数据列表
            format: 输出格式
            output_path: 输出文件路径
            
        Returns:
            str: 合并后的报告内容或文件路径
        """
        logger.info(f"📄 开始合并 {len(reports)} 个报告")
        
        merged_results = []
        for report in reports:
            if "results" in report:
                merged_results.extend(report["results"])
        
        merged_data = {
            "report_type": "merged",
            "generated_at": datetime.now().isoformat(),
            "source_reports": len(reports),
            "summary": self._calculate_merged_summary(reports),
            "results": merged_results
        }
        
        if format == "json":
            content = json.dumps(merged_data, ensure_ascii=False, indent=2)
        else:
            content = await self._generate_merged_html_report(merged_data)
        
        if output_path:
            Path(output_path).parent.mkdir(parents=True, exist_ok=True)
            with open(output_path, 'w', encoding='utf-8') as f:
                f.write(content)
            logger.info(f"✅ 合并报告已保存: {output_path}")
            return output_path
        
        return content
    
    def _calculate_merged_summary(self, reports: List[Dict[str, Any]]) -> Dict[str, Any]:
        """计算合并报告的摘要"""
        total_results = 0
        vulnerable_count = 0
        severity_counts = {"critical": 0, "high": 0, "medium": 0, "low": 0, "info": 0}
        
        for report in reports:
            summary = report.get("summary", {})
            total_results += summary.get("total_results", 0)
            vulnerable_count += summary.get("vulnerable_count", 0)
            
            severity_dist = summary.get("severity_distribution", {})
            for sev in severity_counts:
                severity_counts[sev] += severity_dist.get(sev, 0)
        
        return {
            "total_results": total_results,
            "vulnerable_count": vulnerable_count,
            "not_vulnerable_count": total_results - vulnerable_count,
            "vulnerability_rate": (vulnerable_count / total_results * 100) if total_results > 0 else 0,
            "severity_distribution": severity_counts
        }
    
